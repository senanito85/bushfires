                    <h2>Your Bushfire Plan</h2>
                    <p> Remember. If you are visiting the areas that have significant bush, forest, long grass, or coastal scrub, you need to plan ahead for the fire season. </p>
                    
                    <p>Not everyone thinks clearly in an emergency. Taking steps to get prepared before the fire season means you know what to do when you’re at risk of fire. A written, and preferably well-practised plan, will help you remember what needs to be done during a crisis. </p>
                    <h2> Why should I leave early?<h2>
                    <p> Leaving early is the safest option to protect yourself and your family. Leaving early means leaving the area before there are any signs of a fire – not when you can see flames or smell smoke. Leaving early means avoiding panic, being trapped, making the wrong choices and risking serious injury or death.   </p>

                    <h2> How to plan? </h2>
                    <p>Taking steps to get prepared before the fire season means you know what to do when you’re at risk of fire. Just as every family or household is unique, every fire plan will be different. The information and templates in the Fire Ready Kit and on this website, are designed to assist you.
                    </br>
                    Which Fire Danger Rating is your trigger to leave?</br>
                    Will you leave early that morning or the night before?</br>
                    Where will you go?</br>
                    What route will you take – and what is your alternative in the event that a fire is already in the area?</br>
                    What will you take with you?</br>
                    What do you need to organise for your pets or livestock?</br>
                    Who do you need to keep informed of your movements?</br>
                    Is there anyone outside your household who you need to help or check up on?</br>
                    How will you stay informed about warnings and updates?</br>
                    What will you do if there is a fire in the area and you cannot leave?</br>
                    Remember to decide who will do what as part of your plan.
                 </p>
                 <h2> Your destination and journey </h2>
                 <p> Talk to your family and friends about how you’ll know when to leave and where to go to stay safe. It’s up to you to decide on a suitable place to go when you leave early on a fire risk day. If you do not have friends or relatives in low-risk areas who you can visit, consider community facilities such as libraries, shopping centres, swimming pools or cinemas. If you rely on others for care and support, your only safe option on serious fire risk days is to leave early – never wait to receive a warning.</p>
                 <h2> Why write down your plan?</h2> 
                 <p> Would you remember a plan that’s just in your head if you’re surrounded by smoke, heat and flames? 
A written plan will take the pressure off you, and avoid arguments and delays. In Victoria the bushfire season is long, and a written plan will help reduce uncertainty and anxiety.
Remember, any bushfire plan – written or not – is better than no plan. </p>
